                    for( $i2 = 1; $i2 <= 3; $i2++ ) 
                    {
                        $lines3[] = [
                            $i0, 
                            $i1, 
                            $i2, 
                            0, 
                            0
                        ];
                    }
                }
            }
            $lines4 = [];
            for( $i0 = 1; $i0 <= 3; $i0++ ) 
            {
                for( $i1 = 1; $i1 <= 3; $i1++ ) 
                {
                    for( $i2 = 1; $i2 <= 3; $i2++ ) 
                    {
                        for( $i3 = 1; $i3 <= 3; $i3++ ) 
                        {
                            $lines4[] = [
                                $i0, 
                                $i1, 
                                $i2, 
                                $i3, 
                                0
                            ];
                        }
                    }
                }
            }
            $lines5 = [];
            for( $i0 = 1; $i0 <= 3; $i0++ ) 
            {
                for( $i1 = 1; $i1 <= 3; $i1++ ) 
                {
                    for( $i2 = 1; $i2 <= 3; $i2++ ) 
                    {
                        for( $i3 = 1; $i3 <= 3; $i3++ ) 
                        {
                            for( $i4 = 1; $i4 <= 3; $i4++ ) 
