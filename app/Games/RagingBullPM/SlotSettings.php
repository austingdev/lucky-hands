        public function GetReelStrips($winType, $reelName)
        {
            $filename = base_path() . '/app/Games/RagingBullPM/'.$reelName.'.txt';
            $file = fopen($filename, "r" );
            $filesize = filesize( $filename );
            $content = fread( $file, $filesize);
            $reelStrips = json_decode($content);
            fclose( $file );

            $arrReels = [0,1,2,3,4];

            $endLen = 3;
            $prs = [];
            $reel = [
                'rp' => [],
            ];
            if($winType != 'none')
            {
                if($winType == 'win')
                {
                    $sym = rand(2, 10);
                    $cnt = rand(3, 5);
                    for($i = 0; $i < 5; $i++)
                    {
                        if($i < $cnt)
                            $prs[$i + 1] = $this->GetSymbolPos($reelStrips->reels[$i], $sym, $endLen);
                        else
                            $prs[$i + 1] = mt_rand(1, count($reelStrips->reels[$i]) - $endLen);
                    }                    
                }
                else
                {
                    foreach($arrReels as $i => $value)
                    {
                        if($i >= 1 && $i <= 3)
                            $prs[$i + 1] = $this->GetRandomScatterPos($reelStrips->reels[$value], $endLen, 1);
                        else
                            $prs[$i + 1] = mt_rand(1, count($reelStrips->reels[$value]) - $endLen);
                    }
                }                
            }
            else
            {
                foreach($arrReels as $i => $value)
                {
                    $prs[$i + 1] = mt_rand(1, count($reelStrips->reels[$value]) - $endLen);
                } 
            }
            

            foreach( $prs as $index => $value )
            {
                $key = $reelStrips->reels[$arrReels[$index - 1]];
                $reel['reel' . $index][0] = $key[$value];
                $reel['reel' . $index][1] = $key[$value + 1];
                $reel['reel' . $index][2] = $key[$value + 2];                
                $reel['rp'][] = $value;
            }
            return $reel;
        }
        
